<?php
/**
 * &    AND
 * |    OR
 * ^    XOR
 * ~    NOT
 * <<   Shift left
 * >>   Shift right
 */

var_dump('&',
    0b0101, 0b1010, // 5, 10
    0b0101 & 0b1010 // 0b0000  0
    );

var_dump('|',
    0b1000, 0b1011, // 8, 11
    0b1000 | 0b1011 // 0b1011  11
    );

var_dump('^',
    0b10001, 0b00011, // 17, 3
    0b10001 ^ 0b00011 // 0b10011  18
    );

var_dump('~',
    0b0011, // 3
    ~0b0011, // -4
    decbin(-4),
    decbin(-1) // -1 === ~0
);

var_dump('& ~',
    0b1011, 0b0011, // 11, 3
    0b1011 & ~0b0011 // 0b1000
    );

var_dump('<<',
    0b0011, // 3
    0b0011 << 1, // 0b0110  6
    0b0011 << 2 // 0b1100  12
    );

var_dump('>>',
    0b0011, // 3
    0b0011 >> 1, // 0b0001  1
    0b0011 >> 2 // 0b0000  0
    );

var_dump('>>',
    0b1100, // 12
    0b1100 >> 1, // 0b0110  6
    0b1100 >> 2 // 0b0011  3
    );


var_dump(
    4 << 2 + 1	// 4 << ( 2 + 1 ) == 32
);

